# Raw strings, when you do not want python to interpret escape characters
# Raw strings start with an r
s = r'This is a raw string, \n escape characters do not work '
print(s)
print()
# Multiline strings use """ """ or ''' '''
s = """This is
          a multi-line string using triple double quotes"""
print(s)
print()
s = '''This is a multi-line
         string using triple single quotes'''
print(s)
